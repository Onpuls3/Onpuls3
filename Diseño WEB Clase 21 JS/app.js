// Primer punto
alert ("Hola! Te doy la bienvenida!");
const nombre = prompt ("¿Cual es tu nombre?");
alert ("Hola " + nombre);
const apellido = prompt ("¿Cual es tu apellido?");
alert ("Hola " + nombre + " " + apellido);

// Segundo punto
alert ("Hagamos una suma!");
const primerNumero = prompt ("Introduce un numero");
const segundoNumero = prompt ("Introduce otro numero");
const resultadoSuma = parseFloat (primerNumero) + parseFloat (segundoNumero);
alert ("El resultado es " + resultadoSuma);

// Tercer punto
alert ("Veamos en que año naciste!");
const edadUsuario = prompt ("Introduce tu edad");
const añoActual = "2021";
const resultadoResta = parseFloat (añoActual - edadUsuario);
alert ("Tu naciste en " + resultadoResta);

